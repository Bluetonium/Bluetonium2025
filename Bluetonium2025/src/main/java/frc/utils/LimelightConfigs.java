package frc.utils;

public class LimelightConfigs {
    public final String name;
    public final double xLocation;
    public final double yLocation;
    public final double zLocation;
    public final double pitch;
    public final double yaw;
    public final double roll;

    public LimelightConfigs(String name, double xLocation, double yLocation, double zLocation, double pitch, double yaw,
            double roll) {
        this.name = name;
        this.xLocation = xLocation;
        this.yLocation = yLocation;
        this.zLocation = zLocation;
        this.pitch = pitch;
        this.yaw = yaw;
        this.roll = roll;

    }

}
